    public EditLibraryPlugins(MainFrame mf, Library lib) {
        super(mf, true);
        MF=mf;
        RSC=MF.RSC;
        plugins=MF.RSC.Plugins;
        Lib=lib;
        initComponents();
        DLMs=new ArrayList<DefaultListModel>();
        Lists=new ArrayList<JList>();
        Lists.add(jLManual);
        Lists.add(jLAuto);
        Lists.add(jLBib);
        for (int i=0; i<3; i++) {
            DefaultListModel DLM=new DefaultListModel();
            Lists.get(i).setModel(DLM);
            DLMs.add(DLM);
        }
        fillInModels();
        jLPlugins.setModel(plugins.getPluginsDLM());
        toolbox.centerDialog(this,mf);
    }

    public void addCurrent(int pos) {
        String name=(String)jLPlugins.getSelectedValue();
        if (!DLMs.get(pos).contains(name))
            DLMs.get(pos).addElement(name);
    }

    public void moveUp(int pos) {
        int i=Lists.get(pos).getSelectedIndex();
        if (i>0) {
            String tmp=(String)Lists.get(pos).getSelectedValue();
            DLMs.get(pos).remove(i);
            DLMs.get(pos).add(i-1, tmp);
            Lists.get(pos).setSelectedIndex(i-1);
        }
    }

    public void moveDown(int pos) {
        int i=Lists.get(pos).getSelectedIndex();
        if (i<DLMs.get(pos).getSize()-1) {
            String tmp=(String)Lists.get(pos).getSelectedValue();
            DLMs.get(pos).remove(i);
            DLMs.get(pos).add(i+1, tmp);
            Lists.get(pos).setSelectedIndex(i+1);
        }
    }

    @Override
    public void remove(int pos) {
        int i=Lists.get(pos).getSelectedIndex();
        DLMs.get(pos).remove(i);
    }

    public void fillInModels() {
        String plugins=Lib.MainFile.get("plugins-manual");
        if (plugins!=null) {
            String[] pluginL=plugins.split("\\|");
            for (int i=0;i<pluginL.length;i++)
                DLMs.get(0).addElement(pluginL[i]);
        }
        plugins=Lib.MainFile.get("plugins-auto");
        if (plugins!=null) {
            String[] pluginL=plugins.split("\\|");
            for (int i=0;i<pluginL.length;i++)
                DLMs.get(1).addElement(pluginL[i]);
        }
        plugins=Lib.MainFile.get("plugins-export");
        if (plugins!=null) {
            String[] pluginL=plugins.split("\\|");
            for (int i=0;i<pluginL.length;i++)
                DLMs.get(2).addElement(pluginL[i]);
        }
    }

    public void saveModels() {
        String pluginL=new String("");
        for (int i=0;i<DLMs.get(0).getSize();i++) 
            pluginL+="|"+(String)DLMs.get(0).getElementAt(i);
        if (pluginL.length()>0) pluginL=pluginL.substring(1);
        Lib.MainFile.put("plugins-manual", pluginL);
        pluginL="";
        for (int i=0;i<DLMs.get(1).getSize();i++)
            pluginL+="|"+(String)DLMs.get(1).getElementAt(i);
        if (pluginL.length()>0) pluginL=pluginL.substring(1);
        Lib.MainFile.put("plugins-auto", pluginL);
        pluginL="";
        for (int i=0;i<DLMs.get(2).getSize();i++)
            pluginL+="|"+(String)DLMs.get(2).getElementAt(i);
        if (pluginL.length()>0) pluginL=pluginL.substring(1);
        Lib.MainFile.put("plugins-export", pluginL);
        try {
            Lib.MainFile.writeBack();
        } catch (Exception e) {
            MF.RSC.Msg1.printStackTrace(e);
            toolbox.Warning(this,"Error writing main file!", "Error:");
        }
    }

