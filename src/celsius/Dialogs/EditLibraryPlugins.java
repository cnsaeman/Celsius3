    private void jLPluginsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jLPluginsValueChanged
        String name=(String)jLPlugins.getSelectedValue();
        jTAPlugin.setContentType("text/html");
        jTAPlugin.setText(plugins.getText(name));
        jTAPlugin.setCaretPosition(0);
        String type=plugins.get(name).metaData.get("type");
        if (Parser.EnumContains(type, "manual") || Parser.EnumContains(type, "interactive")) jButton1.setEnabled(true);
        else jButton1.setEnabled(false);
        if (Parser.EnumContains(type, "auto")) jButton5.setEnabled(true);
        else jButton5.setEnabled(false);
        if (Parser.EnumContains(type, "export")) jButton9.setEnabled(true);
        else jButton9.setEnabled(false);
    }//GEN-LAST:event_jLPluginsValueChanged

